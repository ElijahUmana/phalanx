// ────────────────────────────────────────────────────────────────────────────
// Phase 5/6 — Operator (multi-turn approval gate)
// ────────────────────────────────────────────────────────────────────────────

export interface ApprovalOptions {
    /** Pre-recorded human decision — used for automated demos where the
     *  approval is granted non-interactively. If omitted, the Guild CLI
     *  prompts interactively. */
    autoDecision?: 'APPROVE' | 'REJECT' | 'HOLD';
    autoApprover?: string;
    autoReason?: string;
}

export async function runApprovalGate(
    scanId: string,
    validator: ValidatorOutput,
    evidence: EvidenceBundle,
    options: ApprovalOptions = {},
): Promise<OperatorOutput> {
    const meta = loadAgentMeta('phalanx-operator');
    if (!validator.winner) {
        throw new Error(
            'runApprovalGate: validator returned no winner — cannot request approval',
        );
    }

    const prompt = [
        'Validator output:',
        JSON.stringify(validator, null, 2),
        '',
        'Evidence bundle:',
        JSON.stringify(evidence, null, 2),
        '',
        options.autoDecision
            ? `Pre-approved decision: ${options.autoDecision} by ${options.autoApprover ?? 'operator-human'} (reason: ${options.autoReason ?? 'demo'})`
            : 'Present the approval prompt to the human now.',
    ].join('\n');

    const { sessionId, text, durationMs } = await chat('phalanx-operator', prompt);
    const output = operatorOutputSchema.parse(extractJson(text));

    await emitAction(scanId, {
        agentName: 'phalanx-operator',
        agentId: meta.agentId ?? 'unknown',
        sessionId,
        action: `operator.${output.decision.toLowerCase()}`,
        inputHash: hashShort({ validator, evidence }),
        outputHash: hashShort(output),
        durationMs,
    });

    const gateId = `prod-deploy-gate-${validator.cveId}`;
    if (output.decision === 'APPROVE') {
        await emitEvent(scanId, {
            type: 'guild.approval.granted',
            source: 'guild',
            data: {
                gateId,
                approver: output.approver,
                decisionReason: output.reason,
                hypothesisId: output.hypothesisId,
                actions: output.actions,
                productionTouched: output.productionTouched,
            },
        });
    } else {
        await emitEvent(scanId, {
            type: 'guild.approval.denied',
            source: 'guild',
            data: {
                gateId,
                approver: output.approver,
                decision: output.decision,
                reason: output.reason,
                hypothesisId: output.hypothesisId,
                followup: output.followup,
            },
        });
    }

    return output;
}

