// Guild agent orchestration — triggers real Guild sessions for each of the
// five Phalanx agents (scanner, analyst, planner, validator, operator).
//
// Transport: shells out to the `guild agent chat --mode json` CLI with each
// agent's local path. The CLI streams an LLM session against the published
// agent version (or the local unsaved copy when `--ephemeral` is passed).
// Response JSON is parsed through a zod schema at the boundary; orchestrator
// code only ever handles validated, typed output.
//
// Every exported function takes `scanId: string` as the FIRST argument and
// emits:
//   - `guild.action`           per successful agent turn (audit log)
//   - `guild.approval.granted` when the operator returns APPROVE
//   - `guild.approval.denied`  when the operator returns REJECT or HOLD
//
// The AGENT_PATHS resolver looks under $PHALANX_AGENTS_DIR or the default
// "../agents" (relative to the Next.js project root). In production this
// would swap to the Guild HTTP API directly once a stable Node SDK ships.

